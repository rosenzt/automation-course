package Question2;

public class Tail {

    private double length;
    private String color;

    public Tail(double length, String color) {
        this.length = length;
        this.color = color;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
